'''
56. Якщо в одновимірному масиві є три поспіль однакових елемента, то
змінній r привласнити значення істина.
'''

arr = [1, 2, 3, 4, 4, 4, 5, 6, 7, 8, 9]

r = False #начальное значение - ложь

for i in range(len(arr) - 2): #от первого и до второго с конца
    if arr[i] == arr[i + 1] and arr[i + 1] == arr[i + 2]: #если три подряд совпадают
        r = True #значение = истина

print(r)
