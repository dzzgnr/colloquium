'''
5. Створіть масив А[1..7] за допомогою генератора випадкових чисел і
виведіть його на екран. Збільште всі його елементи в 2 рази.
'''

import numpy as np#библиотека для использования функции случайных чисел

A = np.random.randint(100, size = 7) #создание массива из 7 случайных чисел

print('A:', A)

for i in range(7): #удвоение всех элементов массива
    A[i] = 2 * A[i]

print('2*A:', A)
