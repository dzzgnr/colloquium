'''
7. Створіть масив А[1..12] за допомогою генератора випадкових чисел з елементами від -20 до 10 і виведіть його на екран. Замініть всі від’ємні елементи масиву числом 0.
'''

import numpy as np#библиотека для использования функции случайных чисел

A = np.random.randint(-20, 11, size = 12) #создание массива из 12 случайных чисел в диапазоне [-20; 10]

print('A:', A)

for i in range(7): 
    if A[i] < 0: #если элемент меньше нуля - делаем его равным 0
        A[i] = 0

print('A:', A)
