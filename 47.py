'''
47. У лінійному масиві знайти максимальний елемент. Вставте порядковий
номер елемента за ним, пересунувши всі що залишилися на одну позицію вправо.
'''

import numpy as np

arr = [1, 2, 3, 4, 5, 3, 2, 1]

idx = np.where(np.array(arr) == max(np.array(arr)))[0][0] #находим индекс первого максимального элемента

arr.insert(idx + 1, idx) #вставляем индекс элемент после элемента

print(arr)
