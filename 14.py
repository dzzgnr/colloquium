from scipy import constants #раздел библиотеки для использования гравитационной постоянной g

def h(t): #функция для нахождения расстояния в зависимости от времени падения
    return constants.g * t**2 / 2.

heights = [h(t) for t in range(1, 11)] #получение массива с расстояниями

print(heights) #вывол полученного массива
