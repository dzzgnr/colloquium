a = [0, 11, 1, 5, 4, 65, 47, 47, 5, 11]

print('maximal element on even position is', max(a[::2])) #максимальный среди элементов на чётных позициях
if a.count(max(a[::2])) > 1: #если в массиве элементов с таким значением > 1
    print(a.count(max(a[::2])), 'elements') #выводим количество
else:
    print('it is only one') #если нет - сообщаем
